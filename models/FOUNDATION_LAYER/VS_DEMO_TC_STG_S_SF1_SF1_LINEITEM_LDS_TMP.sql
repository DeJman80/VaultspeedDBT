			  "STG_DIS_SRC"."LND_SF1_LINEITEM_HKEY" AS "LND_SF1_LINEITEM_HKEY"
			, "STG_DIS_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
