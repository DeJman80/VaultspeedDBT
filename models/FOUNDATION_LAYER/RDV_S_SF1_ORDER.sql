			, ROW_NUMBER()OVER(PARTITION BY "STG_INR_SRC"."ORDER_HKEY" ORDER BY "STG_INR_SRC"."LOAD_DATE") AS "DUMMY"
		FROM {{ ref('VS_DEMO_TC_STG_ORDER') }} "STG_INR_SRC"
