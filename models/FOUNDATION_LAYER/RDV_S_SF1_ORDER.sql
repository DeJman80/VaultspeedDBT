		, "STG_SRC"."O_ORDERDATE" AS "O_ORDERDATE"
		, "STG_SRC"."O_ORDERPRIORITY" AS "O_ORDERPRIORITY"
		, "STG_SRC"."O_CLERK" AS "O_CLERK"
		, "STG_SRC"."O_SHIPPRIORITY" AS "O_SHIPPRIORITY"
